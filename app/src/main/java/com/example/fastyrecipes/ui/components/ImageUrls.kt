    /**
     * Obtiene una imagen aleatoria basada en la categoría de la receta
     * @param categoria Categoría de la receta (ej: "Cena", "Postre", "Pizza")
     * @return URL de una imagen aleatoria apropiada para la categoría
     */
    fun getImageByCategory(categoria: String): String {
        // Normalizar la categoría (minúsculas, sin espacios extras)
        val categoriaNormalizada = categoria.lowercase().trim()

        // Buscar en las categorías específicas
        imagenesPorCategoria.forEach { (key, imagenes) ->
            if (categoriaNormalizada.contains(key, ignoreCase = true)) {
                return imagenes.random()
            }
        }

        // Buscar en categorías generales
        categoriasGenerales.forEach { (categoriaGeneral, subcategorias) ->
            if (categoriaNormalizada.contains(categoriaGeneral.lowercase())) {
                // Buscar una subcategoría y obtener su imagen
                subcategorias.forEach { subcategoria ->
                    if (imagenesPorCategoria.containsKey(subcategoria)) {
                        return imagenesPorCategoria[subcategoria]!!.random()
                    }
                }
            }
        }

        // Si no se encuentra ninguna coincidencia, devolver una imagen aleatoria general
        return imagenesAleatorias.random()
    }

